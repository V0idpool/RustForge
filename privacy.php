RustForge Privacy Policy
Effective Date: [October 22, 2025]

This Privacy Policy describes how [Void Labs] ("The Developer") collects, uses, and discloses information related to your use of the RustForge application ("RustForge" or "the App"), a third-party utility for creating in-game sign art in Rust.

1. Data RustForge Does Not Collect
RustForge is designed to function primarily as a local desktop application. The Developer does not collect, store, or transmit the following information about your usage:

Personal Identifiable Information (PII): We do not collect your name, address, email, phone number, or any other data that could identify you personally.

Usage Tracking: We do not track which images you convert, how often you use specific features, or your general activity within the App.

Third-Party Analytics: We do not use third-party tools (like Google Analytics) to monitor your use of the application.

2. Information We Collect and How We Use It
RustForge collects and uses data in the following minimal categories, primarily to provide and improve the service.

Data Category: User-Provided Images Purpose and Usage: Images are temporarily loaded into your computer's memory (RAM) and processed on your local machine only to convert them into a format compatible with Rust signs. Images are never stored on the Developerâ€™s servers. Data Collection Method: Local, ephemeral storage (RAM)

Data Category: Configuration Settings Purpose and Usage: Local settings like preferred color tolerance, filters, and hotkeys are saved to a local INI or configuration file on your machine. This is necessary to remember your preferences between sessions. Data Collection Method: Local file storage (e.g., UserCFG.ini)

Data Category: MOTD & Update Checks Purpose and Usage: The App makes a request to download the Message of the Day (MOTD) text.

Data Category: Discord Rich Presence (DRP) ID Purpose and Usage: The App uses your Discord User ID to display your current activity status (e.g., "Painting," "Idle") within Discord. This ID is handled by the Discord RPC library and transmitted to Discord's servers to link the presence to your account. Data Collection Method: Discord RPC Library

Data Category: Premium Access Token Purpose and Usage: If you are a Premium user, a token or identifier may be stored locally or checked against an API endpoint to verify your access to features like VoidWeave or Session Save/Load. Data Collection Method: Local Storage or API Call

3. Data Storage, Security, and Retention
3.1 Local Storage
All your images, painting progress (for Premium users), and configuration settings are stored exclusively on your local computer. The Developer has no access to or control over this data.

3.2 Security
RustForge uses standard, commercially acceptable means to protect its own software code. However, no method of transmission over the Internet or method of electronic storage is 100% secure.

3.3 Data Retention
Since we do not collect or store user data on our servers, there is no retention policy for user-generated content or activity logs related to RustForge use.

4. Third-Party Services
RustForge relies on the following third-party services, each of which has its own Privacy Policy:

Discord: Used for Rich Presence functionality.

Personal Server: Used to host the MOTD file and for update checks.

Buy Me a Coffee / Patreon: Payment processors used for donations and Premium access. These services handle your payment and financial data separately.

